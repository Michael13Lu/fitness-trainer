# ============================================================
# ЭТАП 1: Основы LLM + PromptTemplate
# ============================================================
# Концепции:
#   - ChatOllama       : подключение к локальной модели
#   - PromptTemplate   : шаблон запроса с переменными
#   - invoke()         : вызов модели и получение ответа
# ============================================================

from langchain_ollama import ChatOllama
from langchain_core.prompts import ChatPromptTemplate

# 1. Создаём модель
# model= название модели из Ollama (та что видна в desktop приложении)
# temperature= от 0 (точно/предсказуемо) до 1 (творчески/разнообразно)
llm = ChatOllama(
    model="qwen3-vl:4b-instruct",
    temperature=0.7,
)

# 2. Создаём шаблон prompt
# {topic} и {language} — это переменные, которые мы заполним позже
prompt = ChatPromptTemplate.from_messages([
    ("system", "Ты полезный ассистент. Отвечай на языке: {language}"),
    ("human", "Объясни простыми словами: {topic}"),
])

# 3. Создаём цепочку: prompt -> llm
# Символ | (pipe) соединяет шаги — это основа LangChain
chain = prompt | llm

# 4. Запускаем цепочку с конкретными значениями переменных
print("Отправляем запрос к модели...\n")
response = chain.invoke({
    "topic": "что такое нейронная сеть",
    "language": "русский",
})

# response.content содержит текст ответа
print("Ответ модели:")
print(response.content)

